CREATE TABLE EXCHANGE_CREDENTIALS(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	exchange VARCHAR(60) NOT NULL,
	api_key VARCHAR(60) NOT NULL,
	secret_key VARCHAR(60) NOT NULL,
	passphrase VARCHAR(60) NULL,
	import_date TIMESTAMP
);



CREATE TABLE ACTIONS(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	pair VARCHAR(60) NOT NULL,
	amount VARCHAR(60) NOT NULL,
	import_date TIMESTAMP
);


CREATE TABLE ACCOUNT_INFO(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	okex VARCHAR(60) NOT NULL,
	hitbtc VARCHAR(60) NOT NULL,
	total VARCHAR(60) NOT NULL,
	import_date TIMESTAMP
);

CREATE TABLE ARBITRAGE(
	id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	pair VARCHAR(60) NOT NULL,
	arbitrage VARCHAR(60) NOT NULL,
	import_date TIMESTAMP
);

